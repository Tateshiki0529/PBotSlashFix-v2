	@command(
		name = "okiro",
		description = "ターゲットを起こします [Module: Trolls]",
		guild_ids = GID
	)
	@option(
		name = "target",
		type = Member,
		description = "起こすターゲット (デフォルト: 自分)",
		required = False
	)
	@option(
		name = "count",
		type = int,
		description = "起こす回数 (デフォルト: 5回)",
		required = False
	)
	async def __okiro(self, ctx: ApplicationContext, target: Member = None, count: int = None) -> None:
		if target is None:
			target = ctx.user
		if count is None:
			count = 5
		if count <= 0:
			await ctx.respond(content="回数は1回以上で指定してください！", ephemeral=True)
			return
		
		await ctx.respond(content="今から起こします！ (ターゲット: %s, 回数: %d回)" % (target.display_name, count), ephemeral=True)

		await asleep(3.0)

		for i in range(0, count):
			await ctx.channel.send("%s! 起きろ!" % target.mention)
			await asleep(1.0)
		
		return
	
	# Command: /matsumoto [terminal|normal]
	@command(
		name = "matsumoto",
		description = "まつもとぉ～、まつもとぉ～。 [Module: Trolls]",
		guild_ids = GID
	)
	@option(
		name = "type",
		type = str,
		description = "放送のタイプ (デフォルト: normal)",
		required = False,
		choices = [
			"normal",
			"terminal"
		]
	)
	async def __matsumoto(self, ctx: ApplicationContext, type: str = "normal") -> None:
		match type:
			case "normal":
				text = "%s まつもとぉ～、まつもとぉ～。松本です。" % ctx.user.mention
			case "terminal":
				text = "%s まつもとぉ～、まつもとぉ～、まつもとぉ～。終着、松本です。" % ctx.user.mention
		
		if ctx.user.voice is not None:
			vc = ctx.user.voice.channel
		else:
			vc = None
		
		if ctx.voice_client is None and vc is not None:
			voice = await vc.connect()
		else:
			voice = None
			vc = None
		
		if vc is None:
			await ctx.respond(text)
		else:
			try:
				if len(ctx.channel.members) == 1:
					return
				
				voice.play(FFmpegPCMAudio("./matsumoto_%s.mp3" % type))

				msg: Interaction = await ctx.respond(text)

				while voice.is_playing():
					await asleep(1.0)
			finally:
				await voice.disconnect()
				await msg.delete_original_message()
		return

